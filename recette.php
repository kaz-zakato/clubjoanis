Le Lièvre produit de la chasse, qui carapate
à travers nos Vignes, marque la saison
froide. Pour noël il aura été mijoté au
coin du feu, presque oublié, pour éveiller
une fois à table nos sens terriens et donnera
cette ambiance festive qui réunit à
nouveau la Famille.

1 - Commandez à votre boucher une
cuisse de Lièvre par assiette dont vous
ôterez la chair de façon à obtenir des morceaux
de la taille d'un bouchon de
Champagne. Alors les déposer 6 heures
dans la marinade composée de : 4
Carottes épluchées et taillées en morceaux,
2 gros Oignons émincés, 1 beau
Poireau lavé et taillé en carreaux, 2 feuilles
de Laurier, un beau brin de Thym, une
belle rasade d'huile d'Olive et 2 bouteilles
d'un vin rouge des Côtes du Rhône.

2 - Bien égoutter et colorer les morceaux
de viande à l'Huile d'Olive et Beurre dans
une grande cocotte d'au moins 3 litres.
Une fois bien revenus verser un verre
entier de liqueur de Mûre, laisser réduire
jusqu'à ce quelle soit totalement évaporée
et bien enrober les morceaux de
Lièvre. Puis saupoudrer de Farine (1 cuillère
à soupe), mélanger rapidement à la
cuillère en bois avant de mouiller avec le
vin de la marinade.

3 - Séparément, faire colorer la garniture
restante à la poêle, égoutter puis ajouter
à la cocotte avec une belle pincée de sel.
Couvrir d'un papier sulfurisé taillé en cercle
d'un diamètre supérieur d'au moins 4
cm à celui de la cocotte avec une assiette
renversée par dessus.

4 - Enfourner à four chaud à 120/130°C
pendant environ 3 heures (vérifier de
temps en temps que le civet ne se
réduise pas trop, dans ce cas rajouter un
demi-verre d'eau si nécessaire). Laissez
alors 1 heure de plus si la viande n'est
pas assez fondante. Prévoir du Romarin
fraîchement haché que vous parsèmerez
avant de servir.

5 - Pour les toasts au Foie gras, prévoir
un beau lobe d'environ 600g bien clair et
ferme dans lequel vous taillerez 10 escalopes.
Assaisonner et ne les poêler alors
qu'à la dernière minute, recto-verso,
dans une poêle fumante sans matière
grasse et déposer sur une tranche de
Pain de campagne juste toastée.

6 - En garniture, choisissez plutôt une
purée de Céleri bien blanche pour
accompagner votre civet.


Parlons accords

C'est sur le côté fruit noir que j'ai voulu
travailler, en essayant de lui imposer le
parfum de la Mûre qui me
permet alors de mettre en
contraste 2 éléments de
cette recette établie en fonction
des caractères de ce vin :
- tout d'abord c'est l'apport
d'une note un peu sauvage
grâce au Lièvre et au
Foie gras qui n'est pas
encore présente dans ce
millésime plutôt récent.
Des notes affirmées qui vont
se laisser maîtriser, sans perdre
leur identité par ses arômes de
fruits noirs légèrement sombres à la
douceur et au velouté que son parfum dégage.
- mais également un contraste de
couleurs et d'intensité apporté par la
blancheur de la Crème qui va d'une
part apporter une texture suave et
d'autre part va permettre au parfum
du plat de venir s'appuyer dessus et
ainsi être mis en avant pour réagir
avec le vin.

Sur les textures, pour compenser
le moelleux général
de ce plat, le toast de
Campagne apportera
un croustillant qu'apprécient les
vins jeunes et permettra à cette
recette de sortir d'un enlisement
pâteux que le vin ne réclame pas.

Le Romarin pour sa part, aura pour
rôle, grâce à son aspect végétal et
non-amer de suivre en parallèle le
Vin dans sa longueur, l'accompagnant
et lui restituant la droiture
que l'on attend d'un vin
structuré.
